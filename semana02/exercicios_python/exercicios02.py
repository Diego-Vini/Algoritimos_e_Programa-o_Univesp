##Problema Prático Livro Introdução a Computaçõo Usando Python - Um foco no desenvolvimento de aplicações - Perkovic
# A soma dos 5 primeiros inteiros positivos.
soma = 1 + 2 + 3 + 4 + 5
print("A soma dos numeros inteiro naturais de 1 a 5 é", soma)

#A idade média de Sara (idade 23), Mark (idade 19) e Fátima (idade 31).
idadeSara = 23
idadeMark = 19
idadeFatima = 31
mediaDasIdades = (idadeSara + idadeMark + idadeFatima) / 3
print("A média da soma das idades é", mediaDasIdades)

#O número de vezes que 73 cabe em 403.
n1 = 403
n2 = 73
retornaInteiro = n1 // n2
print("O numero 73 cabe ", retornaInteiro, "vezes")

#O resto de quando 403 é dividido por 73.
retornaResto = n1 % n2
print("O resto da divisão 403 por 73 é", retornaResto)

#2 à 10a potência.
potencia = 2**10
print("A potência de 2 elevado a 10 é", potencia)

#O valor absoluto da distância entre a altura de Sara (54 polegadas) e a altura de Mark (57 polegadas).
alturaSara = 54
alturaMark = 57
distancia = abs(alturaSara - alturaMark)
print("O valor absoluto da distancia entre Mark e Sara é", distancia)

#O menor preço entre os seguintes preços: R$ 34,99, R$ 29,95 e R$ 31,50.
preco = min(34.99, 29.95, 31.50)
print("O menor preço é R$", preco)

